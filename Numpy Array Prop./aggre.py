import numpy as np

arr = np.array([10, 20, 30, 40, 50])

print(np.sum(arr))          # Sum of all elements
print(np.mean(arr))         # Mean of the elements
print(np.median(arr))       # Median of the elements
print(np.std(arr))          # Standard deviation of the elements
print(np.var(arr))          # Variance of the elements
print(np.min(arr))          # Minimum element
print(np.max(arr))          # Maximum element